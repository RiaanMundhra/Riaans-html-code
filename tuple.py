# Tuples in Python

# Tuples are immutable sequences, typically used to store collections of heterogeneous data.

# Tuples are similar to lists, but they cannot be changed after creation.

# Tuples are defined by enclosing the elements in parentheses () separated by commas.

# Tuples can contain elements of different data types, including other tuples.

# Empty tuple

empty_tuple = ()

print(empty_tuple)

# Tuple with characters

char_tuple = ('R', 'i', 'a', 'a', 'n')

print(char_tuple)

# Accessing tuple elements

print(char_tuple[0])

print(char_tuple[1])

# Slicing tuples

print(char_tuple[1:3])